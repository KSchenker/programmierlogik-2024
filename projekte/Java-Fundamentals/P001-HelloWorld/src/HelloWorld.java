void main() {
    // Die folgende Anweisung gibt den Text HelloWorld aus.
    println("Hello World!");
    println("");
    // Mit sogenannten Escape-Sequences lassen sich Zeichen
    // in eine Zeichenkette schreiben, die man nicht direkt
    // per Tastatur eingeben kann (z.B. Zeilenumbruch, Tabstop etc.)
    println ( "Java is awesome!\nI really like it."  ) ;
    // Allgemeine Syntax, um eine Funktion aufzurufen.
    // funktionsname ( argument1, argument2 );
    print("Ein Text ohne Zeilenumbruch am Ende"); // Strg + D
    print("Ein Text ohne Zeilenumbruch am Ende");
}